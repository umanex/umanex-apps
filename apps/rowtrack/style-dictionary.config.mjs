// Format: ts/typography
// ---------------------------------------------------------------------------

function formatTypography() {
  const { raw, flat, setOrder } = loadResolvedTokens();
  const primSet = raw[setOrder[0]] || {};
  const tokSet = raw[setOrder[1]] || {};
  // Semantic type styles live in Theme.type (not Theme.typography)
  const typo = tokSet.type || tokSet.typography || {};

  // fontFamily — always use FONT_FAMILY map (Expo variant names, not token family strings)
  const ffEntries = Object.entries(FONT_FAMILY).map(([k, v]) => [k, lit(v)]);

  // fontSize — from Core.fontSize (not Core.typography.fontSize)
  const fsPrim = primSet.fontSize || primSet.typography?.['font-size'] || primSet.typography?.fontSize || {};
  const fsMap = {};
  for (const k of Object.keys(fsPrim).filter(k => !k.startsWith('$'))) {
    fsMap[k] = flat[`fontSize.${k}`];
  }
  for (const [k, v] of Object.entries(EXTRA_FONT_SIZES)) {
    if (!(k in fsMap)) fsMap[k] = v;
  }
  const fsEntries = Object.keys(fsMap)
    .sort((a, b) => Number(a) - Number(b))
    .map(k => [k, lit(fsMap[k])]);

  // lineHeight — from Core.lineHeight
  const lhPrim = primSet.lineHeight || primSet.typography?.lineHeight || {};
  const lhEntries = Object.keys(lhPrim)
    .filter(k => !k.startsWith('$'))
    .map(k => [k, lit(flat[`lineHeight.${k}`])]);

  // letterSpacing — from Core.letterSpacing. Exported as decimal multipliers (% / 100)
  // so existing code can do: letterSpacing.wider * fontSize['60'] to get px value.
  const lsPrim = primSet.letterSpacing || primSet.typography?.letterSpacing || {};
  const lsEntries = Object.keys(lsPrim)
    .filter(k => !k.startsWith('$'))
    .map(k => {
      const raw = flat[`letterSpacing.${k}`];
      const s = String(raw ?? 0);
      const val = s.endsWith('%') ? parseFloat(s) / 100 : (Number(s) || 0);
      return [k, String(Math.round(val * 10000) / 10000)];
    });

  /** Build semantic styles from tokens or fallback definitions. */
  function semanticStyles(category) {
    // Try tokens first
    const group = typo[category];
    if (group && Object.keys(group).filter(k => !k.startsWith('$')).length > 0) {
      return semanticStylesFromTokens(group, category);
    }
    // Fallback to hardcoded definitions
    const defs = SEMANTIC_TYPOGRAPHY[category];
    if (!defs) return '{}';
    return semanticStylesFromDefs(defs);
  }

  function semanticStylesFromTokens(group, category) {
    // Build lookup from SEMANTIC_TYPOGRAPHY for RN-specific font families + extras
    const fallbackDefs = SEMANTIC_TYPOGRAPHY[category] || [];
    const fallbackMap = {};
    for (const d of fallbackDefs) fallbackMap[d.size] = d;

    const entries = [];
    for (const [size, def] of Object.entries(group)) {
      if (size.startsWith('$')) continue;
      // fontSize + lineHeight from tokens (resolveRef unwraps token objects)
      const fs = Number(resolveRef(def.fontSize));
      const lh = Number(resolveRef(def.lineHeight));
      const computed = Math.round(fs * lh * 100) / 100;
      // fontFamily from SEMANTIC_TYPOGRAPHY (RN variant name with correct weight)
      const fb = fallbackMap[size];
      const ff = fb ? (FONT_FAMILY[fb.ff] || fb.ff) : String(resolveRef(def.fontFamily));
      const props = [`    fontFamily: '${ff}'`, `    fontSize: ${fs}`, `    lineHeight: ${computed}`];
      // letterSpacing: prefer fallback (em-relative), fall back to token value
      if (fb?.ls !== undefined) {
        const lsAbs = Math.abs(fb.ls) < 1 ? Math.round(fs * fb.ls * 100) / 100 : fb.ls;
        props.push(`    letterSpacing: ${lsAbs}`);
      } else if (def.letterSpacing) {
        const lsRaw = Number(resolveRef(def.letterSpacing));
        const lsAbs = Math.abs(lsRaw) < 1 ? Math.round(fs * lsRaw * 100) / 100 : lsRaw;
        props.push(`    letterSpacing: ${lsAbs}`);
      }
      // textTransform from fallback or token
      if (fb?.tt) {
        props.push(`    textTransform: '${fb.tt}' as const`);
      } else if (def.textTransform) {
        props.push(`    textTransform: '${def.textTransform}' as const`);
      }
      const key = /^\d/.test(size) || size.includes('xl') ? `'${size}'` : size;
      entries.push(`  ${key}: {\n${props.join(',\n')},\n  }`);
    }
    return `{\n${entries.join(',\n')},\n}`;
  }

  function semanticStylesFromDefs(defs) {
    const entries = [];
    for (const d of defs) {
      const ff = FONT_FAMILY[d.ff] || d.ff;
      const computed = Math.round(d.fs * d.lh * 100) / 100;
      const props = [`    fontFamily: '${ff}'`, `    fontSize: ${d.fs}`, `    lineHeight: ${computed}`];
      if (d.ls !== undefined) {
        const lsAbs = Math.abs(d.ls) < 1 ? Math.round(d.fs * d.ls * 100) / 100 : d.ls;
        props.push(`    letterSpacing: ${lsAbs}`);
      }
      if (d.tt) props.push(`    textTransform: '${d.tt}' as const`);
      const key = /^\d/.test(d.size) || d.size.includes('xl') ? `'${d.size}'` : d.size;
      entries.push(`  ${key}: {\n${props.join(',\n')},\n  }`);
    }
    return `{\n${entries.join(',\n')},\n}`;
  }

  function resolveRef(val) {
    // Unwrap token objects like {$value: "{...}", $type: "fontSizes"}
    if (isToken(val)) val = tokenValue(val);
    if (typeof val !== 'string') return val;
    const m = val.match(/^\{(.+)\}$/);
    if (m && flat[m[1]] !== undefined) return flat[m[1]];
    return val;
  }

  // --- typeStyles: semantic text styles from Theme.type ---
  const typeEntries = [];
  for (const [name, def] of Object.entries(typo)) {
    if (name.startsWith('$') || !isToken(def)) continue;
    const v = tokenValue(def);
    if (typeof v !== 'object') continue;

    const familyRaw = String(resolveRef(v.fontFamily) || '');
    const weightRawFull = String(resolveRef(v.fontWeight) || '400');
    // Normalize named weights (Bold → 700, SemiBold → 600, …) to their numeric equivalent
    const weightRaw = /^\d+$/.test(weightRawFull)
      ? weightRawFull
      : (NAMED_WEIGHT[weightRawFull] || '400');
    const fs = Number(resolveRef(v.fontSize)) || 0;
    const lhRaw = resolveRef(v.lineHeight);
    const lhFactor = (() => {
      const s = String(lhRaw ?? '');
      if (!s || s === 'AUTO' || s === 'auto') return 1;
      if (s.endsWith('%')) return parseFloat(s) / 100;
      return Number(s) || 1;
    })();
    const lh = Math.round(fs * lhFactor * 100) / 100;
    const lsRaw = resolveRef(v.letterSpacing);
    const ls = lsToAbsPx(lsRaw, fs);
    const hasUppercase = v.textCase === 'uppercase';

    // Italic when explicitly in ITALIC_TYPE_STYLES or fontWeight token is the literal "Italic" style
    const isItalic = ITALIC_TYPE_STYLES.has(name) || weightRawFull === 'Italic';
    const expoKey = isItalic ? `${familyRaw}_${weightRaw}_italic` : `${familyRaw}_${weightRaw}`;
    const expoName = EXPO_FONT_NAME[expoKey] || (isItalic ? `${familyRaw}_${weightRaw}_Italic` : `${familyRaw}_${weightRaw}`);

    const props = [`    fontFamily: '${expoName}'`, `    fontSize: ${fs}`, `    lineHeight: ${lh}`];
    if (ls !== 0) props.push(`    letterSpacing: ${ls}`);
    if (hasUppercase) props.push(`    textTransform: 'uppercase' as const`);

    typeEntries.push(`  ${name}: {\n${props.join(',\n')},\n  }`);
  }
  const typeStylesBody = typeEntries.length > 0
    ? `{\n${typeEntries.join(',\n')},\n}`
    : '{}';

  return `${HEADER}
export const fontFamily = ${obj(ffEntries)} as const;

export const fontSize = ${obj(fsEntries)} as const;

export const lineHeight = ${obj(lhEntries)} as const;

export const letterSpacing = ${obj(lsEntries)} as const;

export const typeStyles = ${typeStylesBody} as const;

export const display = ${semanticStyles('display')} as const;

export const body = ${semanticStyles('body')} as const;

export const label = ${semanticStyles('label')} as const;

export const mono = ${semanticStyles('mono')} as const;

export const typography = {
  fontFamily,
  fontSize,
  lineHeight,
  letterSpacing,
  typeStyles,
  display,
  body,
  label,
  mono,
} as const;
`;
}

