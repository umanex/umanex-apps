// ---------------------------------------------------------------------------
// Composite-token literal builders (typography objects, boxShadow arrays)
// ---------------------------------------------------------------------------
// Composite tokens (DTCG `typography` and `boxShadow`) have object/array values.
// The naive `String(value)` used for scalar tokens collapses these to the
// literal '[object Object]'; these builders resolve them into React-Native-usable
// literals instead (a TextStyle object, resp. an RN `boxShadow` array).

/** Resolve a composite reference (e.g. "{type.buttonPrimary}") to its raw value. */
function derefComposite(ref, flat) {
  if (typeof ref !== 'string') return ref;
  const m = ref.match(/^\{(.+)\}$/);
  return m && flat[m[1]] !== undefined ? flat[m[1]] : ref;
}

/** Convert %-string or numeric letterSpacing to absolute px given fontSize. */
function lsToAbsPx(raw, fs) {
  if (raw == null || raw === '0') return 0;
  const s = String(raw);
  if (s.endsWith('%')) return Math.round(fs * (parseFloat(s) / 100) * 1000) / 1000;
  const n = Number(s);
  return isNaN(n) ? 0 : n;
}

/** Build an RN TextStyle object literal from a typography composite token. */
function typographyLiteral(ref, flat) {
  const comp = derefComposite(ref, flat);
  if (!comp || typeof comp !== 'object') return 'undefined';
  const familyRaw = String(resolve(String(comp.fontFamily ?? ''), flat));
  const weightFull = String(resolve(String(comp.fontWeight ?? '400'), flat));
  const weight = /^\d+$/.test(weightFull) ? weightFull : (NAMED_WEIGHT[weightFull] || '400');
  const fs = Number(resolve(String(comp.fontSize ?? 0), flat)) || 0;
  const lhRaw = resolve(String(comp.lineHeight ?? ''), flat);
  const lhFactor = (() => {
    const s = String(lhRaw ?? '');
    if (!s || s === 'AUTO' || s === 'auto') return 1;
    if (s.endsWith('%')) return parseFloat(s) / 100;
    return Number(s) || 1;
  })();
  const lh = Math.round(fs * lhFactor * 100) / 100;
  const ls = lsToAbsPx(resolve(String(comp.letterSpacing ?? '0'), flat), fs);
  const expoName = EXPO_FONT_NAME[`${familyRaw}_${weight}`] || `${familyRaw}_${weight}`;
  const parts = [`fontFamily: '${expoName}'`, `fontSize: ${fs}`, `lineHeight: ${lh}`];
  if (ls !== 0) parts.push(`letterSpacing: ${ls}`);
  return `{ ${parts.join(', ')} }`;
}

/** Build an RN `boxShadow` array literal from a boxShadow composite token. */
function shadowLiteral(ref, flat) {
  const val = derefComposite(ref, flat);
  const layers = Array.isArray(val) ? val : (val && typeof val === 'object' ? [val] : []);
  if (layers.length === 0) return '[]';
  const num = (x) => Number(x) || 0;
  const items = layers.map((l) => {
    const color = String(resolve(String(l.color ?? ''), flat));
    const inset = l.type === 'innerShadow';
    return `        { offsetX: ${num(l.x)}, offsetY: ${num(l.y)}, blurRadius: ${num(l.blur)}, ` +
      `spreadDistance: ${num(l.spread)}, color: '${color}', inset: ${inset} }`;
  });
  return `[\n${items.join(',\n')},\n      ]`;
}

