function formatSpacing() {
  const { raw, flat, setOrder } = loadResolvedTokens();
  // Raw spacing values live in Core.spacing (not Theme.spacing which doesn't exist)
  const primSet = raw[setOrder[0]] || {};
  const spacingGroup = primSet.spacing || {};

  const spaceEntries = Object.keys(spacingGroup)
    .filter(k => !k.startsWith('$'))
    .sort((a, b) => Number(a) - Number(b))
    .map(k => [k, lit(flat[`spacing.${k}`])]);

  // Backwards-compat Tailwind-style indices (old space used Tailwind scale: 1=4, 2=8, 3=12, 4=16, 5=20, 6=24)
  const TAILWIND_COMPAT = { '1': 4, '3': 12, '5': 20, '7': 28 };
  for (const [k, v] of Object.entries(TAILWIND_COMPAT)) {
    if (!spaceEntries.find(([ek]) => ek === k)) {
      spaceEntries.push([k, lit(v)]);
    }
  }
  // compat: space.px = 1 used for hairline gaps/borders
  if (!spaceEntries.find(([k]) => k === 'px')) {
    spaceEntries.push(['px', '1']);
  }

  // Layout constants derived from Component tokens
  const compSet = raw[setOrder[2]] || {};
  const screenComp = compSet.screen || {};
  const tabBarComp = compSet.tabBar || {};
  const layoutEntries = [
    ['screenPaddingX',    lit(flat['screen.paddingX']    || resolve(String(tokenValue(screenComp.paddingX)  || 24), flat))],
    ['screenPaddingXAlt', lit(flat['screen.paddingXAlt'] || resolve(String(tokenValue(screenComp.paddingXAlt) || 28), flat))],
    ['tabBarPaddingTop',  lit(flat['tabBar.paddingTop']  || resolve(String(tokenValue(tabBarComp.paddingTop) || 18), flat))],
    ['tabBarPaddingBottom', lit(flat['tabBar.paddingBottom'] || resolve(String(tokenValue(tabBarComp.paddingBottom) || 28), flat))],
    // Backwards-compat aliases for pre-existing code
    ['screenHorizontal', '20'],  // was layout.screenHorizontal = 16 in old tokens
    ['navHeight', '48'],         // was layout.navHeight = 48 in old tokens
  ];

  return `${HEADER}
export const space = ${obj(spaceEntries)} as const;

export const layout = ${obj(layoutEntries)} as const;

export const spacing = {
  space,
  layout,
} as const;
`;
}

