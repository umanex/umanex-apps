/**
 * Style Dictionary config — generates constants/*.ts from tokens/tokens.json
 *
 * Handles Figma Tokens Studio multi-set format with cross-set reference
 * resolution. Includes hardcoded fallbacks for React Native-specific values
 * (font variant names, semantic typography compositions) that Tokens Studio
 * does not manage.
 */
import { readFileSync } from 'fs';

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

function loadResolvedTokens() {
  const raw = JSON.parse(readFileSync('tokens/tokens.json', 'utf8'));
  const setOrder = raw.$metadata?.tokenSetOrder || [];

  const flat = {};
  for (const setName of setOrder) {
    const setData = raw[setName];
    if (!setData) continue;
    flattenSet(setData, '', flat);
  }

  for (const key of Object.keys(flat)) {
    flat[key] = resolve(flat[key], flat);
  }

  return { raw, flat, setOrder };
}

function flattenSet(obj, prefix, flat) {
  for (const [key, val] of Object.entries(obj)) {
    if (key.startsWith('$')) continue;
    const path = prefix ? `${prefix}.${key}` : key;
    if (isToken(val)) {
      flat[path] = tokenValue(val);
    } else if (val && typeof val === 'object') {
      flattenSet(val, path, flat);
    }
  }
}

function resolve(value, flat, depth = 0) {
  if (depth > 10) return value;
  if (typeof value !== 'string') return value;
  const m = value.match(/^\{(.+)\}$/);
  if (!m) return value;
  if (flat[m[1]] !== undefined) return resolve(String(flat[m[1]]), flat, depth + 1);
  return value;
}

function lit(v) {
  if (v === undefined || v === null) return "''";
  const str = String(v);
  if (/^#[0-9a-fA-F]{3,8}$/.test(str)) return `'${str.toUpperCase()}'`;
  const n = Number(str);
  if (!isNaN(n) && str !== '' && str !== 'true' && str !== 'false') return String(n);
  return `'${str}'`;
}

function kebabToCamel(s) {
  return s.replace(/-([a-z0-9])/g, (_, c) => c.toUpperCase());
}

function obj(entries, indent = '  ') {
  const lines = entries.map(([k, v]) => {
    const needsQuote = /^\d/.test(k) || !/^[a-zA-Z_$][a-zA-Z0-9_$]*$/.test(k);
    const key = needsQuote ? `'${k}'` : k;
    return `${indent}${key}: ${v},`;
  });
  return `{\n${lines.join('\n')}\n}`;
}

// DTCG-aware token accessors — support W3C DTCG (`$value`/`$type`) and the
// legacy Tokens Studio format (`value`/`type`) transparently.
function isToken(node) {
  return !!node && typeof node === 'object' && ('$value' in node || 'value' in node);
}
function tokenValue(node) {
  if (!node || typeof node !== 'object') return undefined;
  return '$value' in node ? node.$value : node.value;
}
function tokenType(node) {
  if (!node || typeof node !== 'object') return undefined;
  return '$type' in node ? node.$type : node.type;
}

