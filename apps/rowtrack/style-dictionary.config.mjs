function formatColors() {
  const { raw, flat, setOrder } = loadResolvedTokens();
  const primSet = raw[setOrder[0]] || {};
  const tokSet = raw[setOrder[1]] || {};

  function colorScale(colorName) {
    const group = primSet.color?.[colorName] || {};
    return Object.keys(group)
      .filter(k => !k.startsWith('$'))
      .map(k => [k, lit(flat[`color.${colorName}.${k}`])]);
  }

  function semanticColorGroup(groupPath, stripPrefix = '') {
    const parts = groupPath.split('.');
    let group = tokSet;
    for (const p of parts) group = group?.[p] || {};
    return Object.keys(group)
      .filter(k => !k.startsWith('$') && isToken(group[k]))
      .map(k => {
        const cleanKey = stripPrefix && k.startsWith(`${stripPrefix}-`)
          ? k.slice(stripPrefix.length + 1)
          : k;
        return [kebabToCamel(cleanKey), lit(flat[`${groupPath}.${k}`])];
      });
  }

  const neutralEntries = colorScale('neutral');
  // Semantic color groups — resolved from Theme set (bg / fg / accent / border / achievement)
  const bgEntries = semanticColorGroup('bg');
  const fgEntries = semanticColorGroup('fg');
  const accentEntries = semanticColorGroup('accent');
  const borderEntries = semanticColorGroup('border');
  const achievementEntries = semanticColorGroup('achievement');

  // --- Component tokens (from components set) ---
  const compSet = raw[setOrder[2]] || {};
  const buttonGroup = compSet.button || {};
  const buttonVariants = [];

  for (const [variant, def] of Object.entries(buttonGroup)) {
    if (variant.startsWith('$')) continue;
    if (isToken(def)) {
      // top-level prop like radius — skip for buttonTokens
      continue;
    }
    const props = [];
    for (const [prop, val] of Object.entries(def)) {
      if (prop.startsWith('$') || !isToken(val)) continue;
      const type = tokenType(val);
      const raw = tokenValue(val);
      if (type === 'typography') {
        props.push([prop, typographyLiteral(raw, flat)]);
      } else if (type === 'boxShadow') {
        props.push([prop, shadowLiteral(raw, flat)]);
      } else {
        props.push([prop, lit(resolve(String(raw), flat))]);
      }
    }
    // compat: add .text alias for .color so existing components work
    const colorEntry = props.find(([k]) => k === 'color');
    if (colorEntry && !props.find(([k]) => k === 'text')) {
      props.push(['text', colorEntry[1]]);
    }
    if (props.length > 0) {
      const inner = props.map(([k, v]) => `      ${k}: ${v},`).join('\n');
      buttonVariants.push(`    ${variant}: {\n${inner}\n    }`);
    }
  }

  const buttonRadius = isToken(buttonGroup.radius)
    ? `    radius: ${lit(resolve(String(tokenValue(buttonGroup.radius)), flat))},`
    : `    radius: 22,`; // compat: matches primary.radius for shared button borderRadius

  const buttonTokensBody = buttonVariants.length > 0
    ? `{\n${buttonVariants.join(',\n')},\n${buttonRadius ? `${buttonRadius}\n` : ''}  }`
    : '{}';

  // --- progressBar tokens ---
  const progressBarEntries = [
    ['trackColor', lit(flat['progressBar.trackColor'] || '')],
    ['successFill', lit(flat['progressBar.fillSucces'] || '')],  // token has typo: fillSucces
    ['warningFill', lit(flat['progressBar.warningFill'] || '')],
  ];

  return `${HEADER}
export const neutral = ${obj(neutralEntries)} as const;

export const bg = ${obj(bgEntries)} as const;

export const fg = ${obj(fgEntries)} as const;

export const accent = ${obj(accentEntries)} as const;

export const border = ${obj(borderEntries)} as const;

export const achievement = ${obj(achievementEntries)} as const;

export const buttonTokens = ${buttonTokensBody} as const;

export const progressBar = ${obj(progressBarEntries)} as const;

// Backwards-compat aliases — existing components still reference these names
export const background = { ...bg, surface: bg.elevated } as const;
export const text = { ...fg, muted: fg.tertiary, inverse: fg.onAccent } as const;
export const brand = { primary: accent.default, secondary: accent.hover } as const;
export const status = { success: '#22C55E', warning: '#F59E0B', error: '#EF4444' } as const;
export const blue = {} as const;

export const colors = {
  neutral,
  bg,
  fg,
  accent,
  border,
  buttonTokens,
} as const;
`;
}

