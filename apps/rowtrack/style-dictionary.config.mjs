function formatRadius() {
  const { raw, flat, setOrder } = loadResolvedTokens();
  const primSet = raw[setOrder[0]] || {};
  const tokSet = raw[setOrder[1]] || {};

  // Primitive radii from Core.borderRadius
  const borderRadiusGroup = primSet.borderRadius || {};
  const radiiEntries = Object.keys(borderRadiusGroup)
    .filter(k => !k.startsWith('$'))
    .map(k => [k, lit(flat[`borderRadius.${k}`])]);

  // Backwards-compat radii entries
  if (!radiiEntries.find(([k]) => k === '3xl')) {
    radiiEntries.push(['3xl', '32']); // bottom sheet / large sheet corners
  }

  // Component radii from Theme.radius
  const radiusGroup = tokSet.radius || {};
  const componentEntries = Object.keys(radiusGroup)
    .filter(k => !k.startsWith('$') && isToken(radiusGroup[k]))
    .map(k => [kebabToCamel(k), lit(flat[`radius.${k}`])]);

  // Backwards-compat component radius entries
  const COMPONENT_RADIUS_COMPAT = { cardSm: 12, modal: 24 };
  for (const [k, v] of Object.entries(COMPONENT_RADIUS_COMPAT)) {
    if (!componentEntries.find(([ek]) => ek === k)) {
      componentEntries.push([k, String(v)]);
    }
  }

  return `${HEADER}
export const radii = ${obj(radiiEntries)} as const;

export const componentRadius = ${obj(componentEntries)} as const;

export const radius = {
  radii,
  component: componentRadius,
} as const;
`;
}

